package com.sbss.crm.franchisee.model;

public class FranchiseeVO {
	
	private int rnum;
	
	private int franchiseeId;
	
	private String franchiseeName;
	
	private String phoneNumber;
	
	private String cellphoneNumber;
	
	private String postalCode;
	
	private String address;
	
	private String franchiseeStatus;
	
	private String franchiseeStatusName;

	public int getRnum() {
		return rnum;
	}

	public void setRnum(int rnum) {
		this.rnum = rnum;
	}

	public int getFranchiseeId() {
		return franchiseeId;
	}

	public void setFranchiseeId(int franchiseeId) {
		this.franchiseeId = franchiseeId;
	}

	public String getFranchiseeName() {
		return franchiseeName;
	}

	public void setFranchiseeName(String franchiseeName) {
		this.franchiseeName = franchiseeName;
	}

	public String getPhoneNumber() {
		return phoneNumber;
	}

	public void setPhoneNumber(String phoneNumber) {
		this.phoneNumber = phoneNumber;
	}

	public String getCellphoneNumber() {
		return cellphoneNumber;
	}

	public void setCellphoneNumber(String cellphoneNumber) {
		this.cellphoneNumber = cellphoneNumber;
	}

	public String getPostalCode() {
		return postalCode;
	}

	public void setPostalCode(String postalCode) {
		this.postalCode = postalCode;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getFranchiseeStatus() {
		return franchiseeStatus;
	}

	public void setFranchiseeStatus(String franchiseeStatus) {
		this.franchiseeStatus = franchiseeStatus;
	}

	public String getFranchiseeStatusName() {
		return franchiseeStatusName;
	}

	public void setFranchiseeStatusName(String franchiseeStatusName) {
		this.franchiseeStatusName = franchiseeStatusName;
	}

}
