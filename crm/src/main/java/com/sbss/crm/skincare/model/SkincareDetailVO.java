package com.sbss.crm.skincare.model;

public class SkincareDetailVO {
	
	private int skincareDetailId;
	
	private int cycle;
	
	private String manageDate;
	
	private String memo;
	
	private int optionPrice;
	
	private int paymentId;
	
	private int skincareId;
	
	private int manageEmployeeId;

	private int employeeId;

	public int getSkincareDetailId() {
		return skincareDetailId;
	}

	public void setSkincareDetailId(int skincareDetailId) {
		this.skincareDetailId = skincareDetailId;
	}

	public int getCycle() {
		return cycle;
	}

	public void setCycle(int cycle) {
		this.cycle = cycle;
	}

	public String getManageDate() {
		return manageDate;
	}

	public void setManageDate(String manageDate) {
		this.manageDate = manageDate;
	}

	public String getMemo() {
		return memo;
	}

	public void setMemo(String memo) {
		this.memo = memo;
	}

	public int getOptionPrice() {
		return optionPrice;
	}

	public void setOptionPrice(int optionPrice) {
		this.optionPrice = optionPrice;
	}

	public int getPaymentId() {
		return paymentId;
	}

	public void setPaymentId(int paymentId) {
		this.paymentId = paymentId;
	}

	public int getSkincareId() {
		return skincareId;
	}

	public void setSkincareId(int skincareId) {
		this.skincareId = skincareId;
	}

	public int getManageEmployeeId() {
		return manageEmployeeId;
	}

	public void setManageEmployeeId(int manageEmployeeId) {
		this.manageEmployeeId = manageEmployeeId;
	}

	public int getEmployeeId() {
		return employeeId;
	}

	public void setEmployeeId(int employeeId) {
		this.employeeId = employeeId;
	}
	
}
